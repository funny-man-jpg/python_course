def perimeter(a,b):
  """Calculates the perimeter given the 2 sides

  Parameters
  ----------
  a : float
    The larger side in m

  b : float
    The smaller side in m

  Returns
  -------
  float
    the perimeter in 2a+2b
  """
  return a*2+b*2
def area(b,h):
  """Calculates the area given the base and height
  
  Parameters
  ----------
  b : float
    The base in m

  h : float
    The height in m

  Returns
  -------
  float
    the area in b*h
  """
  return b*h