def perimeter(a,b,c):
  """Calculates the perimeter given the 3 sides
  
  Parameters
  ----------
  a : float
    The first side in m

  b : float
    The second side in m

  c
    The third side in m

  Returns
  -------
  float
    the perimeter in a+b+c
  """
  return a+b+c
def area(b,h):
  """Calculates the area given the base and height
  
  Parameters
  ----------
  b : float
    The base in m

  h : float
    The height in m

  Returns
  -------
  float
    the area in b*h/2
  """
  return (b*h)/2