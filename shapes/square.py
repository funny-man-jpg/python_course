
def perimeter(w):
  """Calculates the perimeter given the width
  
  Parameters
  ----------
  w : float
    The base in m

  Returns
  -------
  float
    the perimeter in 4w
  """
  return w*4
def area(w):
  """Calculates the area given the width
  
  Parameters
  ----------

  w : float
    The width in m

  Returns
  -------
  float
    the area in w^2
  """
  return w**2