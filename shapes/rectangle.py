
def perimeter(w,h):
  """Calculates the perimeter given the width and height
  
  Parameters
  ----------
  w : float
    The width in m

  h : float
    The height in m

  Returns
  -------
  float
    the perimeter in 2w+2h
  """
  return 2*w+2*h
def area(w,h):
  """Calculates the area given the width and height
  
  Parameters
  ----------
  w : float
    The width in m

  h : float
    The height in m

  Returns
  -------
  float
    the area in w*h
  """
  return w*h